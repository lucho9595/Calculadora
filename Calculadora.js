// Como es una clase donde aca se va a alojar las operaciones matematicas que realiza la calculadora
// Va a empezar con mayuscula.

class Calculadora {
    sumar(number1, number2){
        return number1 + number2;
    }

    restar(number1, number2){
        return number1 - number2;
    }

    dividir(number1, number2){
        return number1 / number2;
    }
multiplicar(number1, number2){
        return number1 * number2;
    }
}